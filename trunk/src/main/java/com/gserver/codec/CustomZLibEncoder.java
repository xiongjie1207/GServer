import java.io.ByteArrayOutputStream;
import java.io.IOException;
import java.util.zip.Deflater;

import io.netty.buffer.ByteBuf;
import io.netty.channel.ChannelHandler;
import io.netty.channel.ChannelHandlerContext;
import io.netty.handler.codec.MessageToByteEncoder;
@ChannelHandler.Sharable
public class CustomZLibEncoder extends MessageToByteEncoder<ByteBuf> {

	/**
	 * 压缩
	 * 
	 * @param data
	 *            待压缩数据
	 * @return byte[] 压缩后的数据
	 */
	private byte[] compress(byte[] data) {
		byte[] output = new byte[0];

		Deflater compresser = new Deflater();

		compresser.reset();
		compresser.setInput(data);
		compresser.finish();
		ByteArrayOutputStream bos = new ByteArrayOutputStream(data.length);
		try {

			while (!compresser.finished()) {
				byte[] buf = new byte[1024];
				int i = compresser.deflate(buf);
				bos.write(buf, 0, i);
			}
			output = bos.toByteArray();
		} catch (Exception e) {
			output = data;
			e.printStackTrace();
		} finally {
			try {
				bos.close();
			} catch (IOException e) {
				e.printStackTrace();
			}
		}
		compresser.end();
		return output;
	}


	@Override
	protected void encode(ChannelHandlerContext paramChannelHandlerContext, ByteBuf paramI,
						  ByteBuf paramByteBuf) throws Exception {
		// TODO Auto-generated method stub
		byte[] data = new byte[paramI.readableBytes()];
		paramI.readBytes(data);
		paramByteBuf.writeBytes(compress(data));
	}
}
